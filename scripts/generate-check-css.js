#!/usr/bin/env node
'use strict';

const fs = require('fs');
const path = require('path');

const FILES = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h'];

function sqName(file, rank) {
  return FILES[file] + (rank + 1);
}

function isValid(file, rank) {
  return file >= 0 && file < 8 && rank >= 0 && rank < 8;
}

const KNIGHT_OFFSETS = [
  [-2, -1], [-2, 1], [-1, -2], [-1, 2],
  [1, -2], [1, 2], [2, -1], [2, 1],
];

const ROOK_DIRS = [[0, 1], [0, -1], [1, 0], [-1, 0]];
const BISHOP_DIRS = [[1, 1], [1, -1], [-1, 1], [-1, -1]];

/**
 * Generate check detection CSS.
 * For each possible king position and each attacker position,
 * emit a rule that sets --wk-in-check: 1 or --bk-in-check: 1 on #board.
 */
function generateCheckCss() {
  const allRules = [];

  allRules.push('/* CSS Chess Engine — Check Detection Rules');
  allRules.push(' * Auto-generated by scripts/generate-check-css.js');
  allRules.push(' * Sets --wk-in-check or --bk-in-check on #board when a king is in check.');
  allRules.push(' */');
  allRules.push('');
  allRules.push('/* Default: no check */');
  allRules.push('#board { --wk-in-check: 0; --bk-in-check: 0; }');
  allRules.push('');

  let totalRules = 0;

  // For each defending color (the king being checked)
  for (const defColor of ['w', 'b']) {
    const atkColor = defColor === 'w' ? 'b' : 'w';
    const king = defColor + 'K';
    const varName = defColor === 'w' ? '--wk-in-check' : '--bk-in-check';
    const colorName = defColor === 'w' ? 'White' : 'Black';

    // For each possible king position
    for (let kf = 0; kf < 8; kf++) {
      for (let kr = 0; kr < 8; kr++) {
        const kingSq = sqName(kf, kr);

        // Knight checks
        for (const [df, dr] of KNIGHT_OFFSETS) {
          const af = kf + df;
          const ar = kr + dr;
          if (!isValid(af, ar)) continue;
          const atkSq = sqName(af, ar);
          const sel = `#board:has(.sq[data-s="${kingSq}"][data-p="${king}"]):has(.sq[data-s="${atkSq}"][data-p="${atkColor}N"]) { ${varName}: 1; }`;
          allRules.push(sel);
          totalRules++;
        }

        // Pawn checks (pawns attack diagonally)
        {
          const pawnDir = defColor === 'w' ? 1 : -1; // direction pawn attacks FROM (opponent pawns)
          for (const df of [-1, 1]) {
            const af = kf + df;
            const ar = kr + pawnDir; // opponent pawn is one rank "ahead" in its attack direction
            if (!isValid(af, ar)) continue;
            const atkSq = sqName(af, ar);
            const sel = `#board:has(.sq[data-s="${kingSq}"][data-p="${king}"]):has(.sq[data-s="${atkSq}"][data-p="${atkColor}P"]) { ${varName}: 1; }`;
            allRules.push(sel);
            totalRules++;
          }
        }

        // Rook and Queen checks (along ranks and files)
        for (const [df, dr] of ROOK_DIRS) {
          let af = kf + df;
          let ar = kr + dr;
          const between = [];
          while (isValid(af, ar)) {
            const atkSq = sqName(af, ar);

            // Build selector: king at kingSq, all between empty, attacker is rook or queen
            let sel = `#board:has(.sq[data-s="${kingSq}"][data-p="${king}"])`;
            for (const bsq of between) {
              sel += `:has(.sq[data-s="${bsq}"][data-p="empty"])`;
            }
            sel += `:has(.sq[data-s="${atkSq}"]:is([data-p="${atkColor}R"],[data-p="${atkColor}Q"]))`;
            sel += ` { ${varName}: 1; }`;
            allRules.push(sel);
            totalRules++;

            between.push(atkSq);
            af += df;
            ar += dr;
          }
        }

        // Bishop and Queen checks (along diagonals)
        for (const [df, dr] of BISHOP_DIRS) {
          let af = kf + df;
          let ar = kr + dr;
          const between = [];
          while (isValid(af, ar)) {
            const atkSq = sqName(af, ar);

            let sel = `#board:has(.sq[data-s="${kingSq}"][data-p="${king}"])`;
            for (const bsq of between) {
              sel += `:has(.sq[data-s="${bsq}"][data-p="empty"])`;
            }
            sel += `:has(.sq[data-s="${atkSq}"]:is([data-p="${atkColor}B"],[data-p="${atkColor}Q"]))`;
            sel += ` { ${varName}: 1; }`;
            allRules.push(sel);
            totalRules++;

            between.push(atkSq);
            af += df;
            ar += dr;
          }
        }
      }
    }

    allRules.push(`/* End ${colorName} king check detection */`);
    allRules.push('');
  }

  const css = allRules.join('\n');
  const outPath = path.join(__dirname, '..', 'css', 'check-detection.css');
  fs.writeFileSync(outPath, css, 'utf8');

  console.log(`Generated ${outPath}`);
  console.log(`Total check-detection rules: ${totalRules}`);
}

if (require.main === module) {
  generateCheckCss();
}

module.exports = { generateCheckCss };
